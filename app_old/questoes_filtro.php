  // Função para criar um observador de mutação na lista
  function observeList(listId) {
    var list = document.getElementById(listId);

    // Verifica a lista assim que o observador é configurado
    checkListAndShowContent(listId);

    // Configura o observador de mutação para verificar a lista continuamente
    var observer = new MutationObserver(function (mutationsList) {
      checkListAndShowContent(listId);
    });
    
